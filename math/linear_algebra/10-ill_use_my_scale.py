#!/usr/bin/env python3
"""
Defines a function that returns the shape of a numpy.ndarray.
"""


def np_shape(matrix):
    """
    Returns the shape of a numpy.ndarray as a tuple of integers.

    Args:
        matrix (numpy.ndarray): The input array.
    Returns:
        tuple: A tuple representing the shape of the array.
    """
    return matrix.shape
